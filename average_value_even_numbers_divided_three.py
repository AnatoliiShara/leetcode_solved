import math 
from typing import List

def averageValue(nums: List[int]) -> int:
	summa = 0 
	count = 0
	for num in nums:
		if num % 6 == 0:
			summa += num
			count += 1
	if count == 0:
		return 0 
	return math.floor(summa / count)
print(averageValue([1,3,6,10,12,15]))

